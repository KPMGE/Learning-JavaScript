// sample with join method

const arr = [1, 2, 3, 4, 5];

console.log("Original array: ", arr);
console.log(`Comma-separated string: ${arr.join()}`);
console.log(`Space-separated string: ${arr.join(" ")}`);
console.log(`Non-separated string: ${arr.join("")}`);
console.log(`Dash-separated string: ${arr.join("-")}`);
