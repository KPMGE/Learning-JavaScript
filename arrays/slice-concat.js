// slice take elements between 2 values of index
let arr = [1, 3, 8, 19, 59];

console.log("Original array: ", arr);
console.log("The values between positions 1 and 4 on array are: ", arr.slice(1, 4));
console.log("The values of 1 position until the end are: ", arr.slice(1));


// concat looks like + operator to strings
let arr2 = [34, 513];

console.log("Second array: ", arr2);
console.log("Firs array concatenated with second one: ", arr.concat(arr2));
